#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new;
	size_t	i;
	size_t	j;

	if (s == NULL)
		return (NULL);
	new = ft_strnew(ft_strlen(s));
	if (new == NULL)
		return (NULL);
	i = 0;
	j = ft_strlen(s);
	while (i < j)
	{
		new[i] = f((unsigned int)i, (char)s[i]);
		i++;
	}
	return (new);
}
