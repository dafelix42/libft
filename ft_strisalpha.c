#include "libft.h"

int	ft_strisalpha(char *str)
{
	if (!*str)
		return (0);
	while (*str)
	{
		if ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
			str++;
		else
			return (0);
	}
	return (1);
}
