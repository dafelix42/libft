#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	val;
	int	i;

	sign = 1;
	val = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		val = val * 10 + (str[i] - '0');
		i++;
	}
	return (val * sign);
}
