#include "libft.h"

size_t	ft_countline(char *str)
{
	size_t	nb;

	nb = 1;
	if (!*str)
		return (0);
	while (*str)
	{
		if (*str == '\n')
			nb++;
		str++;
	}
	return (nb);
}
