#include "libft.h"

size_t	ft_strposic(const char *s, char c)
{
	size_t i;

	i = 0;
	while (s[i] != '\0' && s[i] != c)
		i++;
	if (s[i] == '\0')
		return (0);
	return (i + 1);
}
